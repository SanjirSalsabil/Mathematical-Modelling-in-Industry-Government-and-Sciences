lambda=1.0142;
close all
N0=6766907.5/1000000;

n=-(1700-2015);
for i=1:n
    N(i)=lambda^(i)*N0;
end
x=[1700:2015]';
[xd,yd]=data();
plot(xd,yd./1000000,'-*','LineWidth',2)
axis([1700 2020 0 600])
hold on
plot(x(1:10:end),N(1:10:end)','-','LineWidth',2);
axis([1700 2020 0 600])
title('North and Central America Population')
legend('Real Data','Malthus Model');
xlabel('Time (Years)')
ylabel('Population (Millions)')
set(gca,'fontsize', 16)

